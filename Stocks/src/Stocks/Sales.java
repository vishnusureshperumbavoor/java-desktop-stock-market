    private void btnaddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnaddActionPerformed
        // TODO add your handling code here:
        int price = Integer.parseInt(txtprice.getText());
        int quantity = Integer.parseInt(txtquantity.getText());
        int total = price * quantity;
        df = (DefaultTableModel)tblpurchases.getModel();
        df.addRow(new Object[]
        {
            txtid.getText(),
            txtname.getText(),
            txtprice.getText(),
            txtquantity.getText(),
            total
            
        });
        int sum=0;
        for(int i=0;i<tblpurchases.getRowCount();i++){
            sum += Integer.parseInt(tblpurchases.getValueAt(i,4).toString());
        }
        txtcost.setText(String.valueOf(sum));
    }//GEN-LAST:event_btnaddActionPerformed

    private void btnpurchasesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnpurchasesActionPerformed
        try {
            // TODO add your handling code here:
            DateTimeFormatter dtf = DateTimeFormatter.ofPattern("yyyy/MM/dd");
            LocalDateTime now = LocalDateTime.now();
            String date = dtf.format(now);
            
            String total = txtcost.getText();
            String customer = cbpurchase.getSelectedItem().toString();
            String payment = txtpayment.getText();
            String due = txtdue.getText();
            int lastid = 0 ;
            String query = "insert into purchase (date,customer,total,payment,due) values (?,?,?,?,?)";
            pst = conn.prepareStatement(query,Statement.RETURN_GENERATED_KEYS);
            pst.setString(1, date);
            pst.setString(2, customer);
            pst.setString(3, total);
            pst.setString(4, payment);
            pst.setString(5, due);
            pst.executeUpdate();
            rs = pst.getGeneratedKeys();
            if(rs.next()){
                lastid = rs.getInt(1);
            }
            
            String query2 = "insert into purchaseitem (purchaseid,stockid,price,quantity,total) values (?,?,?,?,?)";
            pst2 = conn.prepareStatement(query2);
            String stockid;
            String price;
            String quantity;
            int ttotal = 0;
            for(int i=0;i<tblpurchases.getRowCount();i++){
                stockid = (String)tblpurchases.getValueAt(i,0);
                price = (String)tblpurchases.getValueAt(i,2);
                quantity = (String)tblpurchases.getValueAt(i,3);
                ttotal = (int)tblpurchases.getValueAt(i,4);
                
                pst2.setInt(1, lastid);
                pst2.setString(2,stockid);
                pst2.setString(3, price);
                pst2.setString(4, quantity);
                pst2.setInt(5,ttotal);
                pst2.executeUpdate();
            }            
           JOptionPane.showMessageDialog(this,"purchase completed");
           
           
        } catch (SQLException ex) {
            Logger.getLogger(Sales.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnpurchasesActionPerformed

