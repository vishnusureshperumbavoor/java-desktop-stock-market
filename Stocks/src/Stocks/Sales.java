    public void Customers(){
        try {
            pst = conn.prepareStatement("select username from customers");
            rs = pst.executeQuery();
            cbpurchase.removeAllItems();
            while(rs.next()){
                cbpurchase.addItem(rs.getString("username"));
            }
        } catch (SQLException ex) {
            Logger.getLogger(searchStocks.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
