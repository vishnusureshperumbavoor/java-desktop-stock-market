    private void txtcatupdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtcatupdateActionPerformed
        // TODO add your handling code here:
        DefaultTableModel dl = (DefaultTableModel)tblsector.getModel();
        int index = tblsector.getSelectedRow();
        int id = Integer.parseInt(dl.getValueAt(index,0).toString());
            
        String category = txtsector.getText();
                try{
            pre = conn.prepareStatement("update sectors set sector=? where sectorid=?");
               pre.setString(1, category);
               pre.setInt(2, id);
               
               int rs = pre.executeUpdate();
               
               if(rs==1){
                   JOptionPane.showMessageDialog(null, "sector updated successfull");
                   fetch();
                   clear();
                   txtsector.requestFocus();
               }
        }
        catch(Exception e){
            System.out.println(e);
        }
    }//GEN-LAST:event_txtcatupdateActionPerformed

    private void txtcatdeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtcatdeleteActionPerformed
        // TODO add your handling code here:
        DefaultTableModel dl = (DefaultTableModel)tblsector.getModel();
        int index = tblsector.getSelectedRow();
        int id = Integer.parseInt(dl.getValueAt(index,0).toString());
        int dialogResult = JOptionPane.showConfirmDialog(null,"do u want to delete","warning",JOptionPane.YES_NO_OPTION);
        if(dialogResult==JOptionPane.YES_OPTION){
            try{
            String sql = "delete from sectors where sectorid='"+id+"'";
            Statement stmt = (Statement) conn.createStatement();
               int rs = stmt.executeUpdate(sql);
               if(rs==1){
                   JOptionPane.showMessageDialog(this,"Successfully deleted");
                   fetch();
                   clear();
                   txtsector.requestFocus();
               }
            
        }
        catch(Exception e){
            System.out.println(e);
        }
        }
    }//GEN-LAST:event_txtcatdeleteActionPerformed

