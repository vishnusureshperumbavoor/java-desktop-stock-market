    private void fetch(){
        try{
             stmt = conn.createStatement();
             rs = stmt.executeQuery("select * from sectors");
             DefaultTableModel dl = (DefaultTableModel)tblsector.getModel();
             dl.setRowCount(0);
             while(rs.next()){
                 Vector v = new Vector();
                 v.add(rs.getString("sectorid"));
                 v.add(rs.getString("sector"));
                 dl.addRow(v);
             }                
        }
        catch(Exception e){
                     System.out.println("error" + e);
         }
    }
