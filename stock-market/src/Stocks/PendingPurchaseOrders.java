        public void fetch()
        {
          try
        {
            String sql="select p.*,c.*,s.* from purchaseorder p inner join customers c on p.customerid = c.customerid inner join stocks s on p.stockid = s.stockid where p.orderstatus = 'pending' ";
            System.out.println(sql);
            Statement stmt=conn.createStatement();
            ResultSet rs= stmt.executeQuery(sql);
            System.out.println(rs);
            DefaultTableModel d1=(DefaultTableModel)tblpurchaseorder.getModel();
            d1.setRowCount(0);
            while(rs.next())
                    {
                        Vector v2=new Vector();
                        v2.add(rs.getString("p.id"));
                        v2.add(rs.getString("c.customerid"));
                        v2.add(rs.getString("c.username"));
                        v2.add(rs.getString("c.demat"));
                        v2.add(rs.getString("s.stockid"));
                        v2.add(rs.getString("s.name"));
                        v2.add(rs.getString("s.supply"));
                        v2.add(rs.getString("s.price"));
                        v2.add(rs.getString("p.targetprice"));
                        v2.add(rs.getString("p.shares"));
                        v2.add(rs.getString("p.date"));
                        v2.add(rs.getString("p.time"));
                        v2.add(rs.getString("p.orderstatus"));
                        d1.addRow(v2);
                    }
        }
        catch(Exception e)
        {
            System.out.println("Error" + e);

        } 
        }
