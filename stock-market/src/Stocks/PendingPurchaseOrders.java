    private void btnpurchaseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnpurchaseActionPerformed
        // TODO add your handling code here:
        DefaultTableModel dl=(DefaultTableModel)tblpurchaseorder.getModel();
            int index=tblpurchaseorder.getSelectedRow();
            DateTimeFormatter dtf = DateTimeFormatter.ofPattern("yyyy/MM/dd");
            DateTimeFormatter dtf2 = DateTimeFormatter.ofPattern("HH:mm:ss a");
            LocalDateTime now = LocalDateTime.now();
            String date = dtf.format(now);
            String time = dtf2.format(now);
            int id=Integer.parseInt(dl.getValueAt(index,0).toString());
            int cusid = Integer.parseInt(dl.getValueAt(index,1).toString());
            int balance = Integer.parseInt(dl.getValueAt(index,3).toString());
            int stockid = Integer.parseInt(dl.getValueAt(index,4).toString());
            int supply = Integer.parseInt(dl.getValueAt(index,6).toString());
            int currentprice = Integer.parseInt(dl.getValueAt(index,7).toString());
            int targetprice = Integer.parseInt(dl.getValueAt(index,8).toString());
            int shares = Integer.parseInt(dl.getValueAt(index,9).toString());
            int total = targetprice * shares;
            if(balance<total){
                JOptionPane.showMessageDialog(null,"Not enough balance in demat account. So order cannot be executed");
            }
            else if(supply<shares){
                JOptionPane.showMessageDialog(null,"Out of stock. So order cannot be executed");
            }else if(currentprice>=targetprice){
                JOptionPane.showMessageDialog(null,"Current prize is greater than target prize. Sellers not available");
            }
            else{
        try {
            String sql = "insert into purchases values ("+null+",'"+cusid+"','"+stockid+"','"+date+"','"+time+"','"+targetprice+"','"+shares+"')";
            pst = conn.prepareStatement(sql);
            pst.executeUpdate();
            
            pst = conn.prepareStatement("select * from pool where cusid = '"+cusid+"' and stockid = '"+stockid+"'");
            rs = pst.executeQuery();
            if(rs.next()==false){
                pst = conn.prepareStatement("insert into pool values ("+null+",'"+cusid+"','"+stockid+"','"+0+"','"+0+"')");
                pst.executeUpdate();
            }
            
            pst = conn.prepareStatement("update pool set amount = amount + '"+total+"', shares = shares + '"+shares+"' where cusid = '"+cusid+"' and stockid = '"+stockid+"'");
            pst.executeUpdate();
            
            pst = conn.prepareStatement("update customers set demat = demat - '"+total+"' where customerid = '"+cusid+"'");
            pst.executeUpdate();
            
            double inc = total * 0.001;
            pst = conn.prepareStatement("update stocks set supply = supply - '"+shares+"',price = price + '"+inc+"' where stockid = '"+stockid+"'");
            pst.executeUpdate();
            
            pst = conn.prepareStatement("update purchaseorder set orderstatus='successful' where id='"+id+"'");
            pst.executeUpdate();
            JOptionPane.showMessageDialog(null,"purchase order executed successfully");
            fetch();
        } catch (SQLException ex) {
            Logger.getLogger(Sales.class.getName()).log(Level.SEVERE, null, ex);
        }
            }
    }//GEN-LAST:event_btnpurchaseActionPerformed

